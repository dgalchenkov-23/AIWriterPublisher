        private async Task<TechnicalSpecDto> CallOllamaAsync(string systemPrompt, string userPrompt)
        {
            string ollamaUrl = _configuration["AIServices:Ollama:BaseUrl"] ?? "http://localhost:11434";
            string modelName = _configuration["AIServices:Ollama:Model"] ?? "qwen2.5-coder:7b";
            
            // Формируем полный промпт (Ollama не разделяет system/user в стандартном API generate)
            string fullPrompt = $"Системная инструкция:\n{systemPrompt}\n\nЗапрос пользователя:\n{userPrompt}\n\nОтветь ТОЛЬКО JSON, без пояснений:";
            
            var requestBody = new
            {
                model = modelName,
                prompt = fullPrompt,
                stream = false,
                options = new
                {
                    temperature = 0.1,
                    num_predict = 4000  // max tokens
                }
            };
            
            string jsonPayload = JsonSerializer.Serialize(requestBody);
            
            var request = new HttpRequestMessage(HttpMethod.Post, $"{ollamaUrl.TrimEnd('/')}/api/generate");
            request.Content = new StringContent(jsonPayload, Encoding.UTF8, "application/json");
            
            Console.WriteLine($"[Ollama] Отправка запроса к {ollamaUrl}/api/generate, модель: {modelName}");
            
            var response = await _httpClient.SendAsync(request);
            
            if (!response.IsSuccessStatusCode)
            {
                string error = await response.Content.ReadAsStringAsync();
                Console.WriteLine($"[Ollama Error] {response.StatusCode}: {error}");
                return new TechnicalSpecDto { Subject = userPrompt };
            }
            
            string responseString = await response.Content.ReadAsStringAsync();
            Console.WriteLine($"[Ollama] Получен ответ: {responseString.Substring(0, Math.Min(200, responseString.Length))}...");
            
            using var doc = JsonDocument.Parse(responseString);
            var root = doc.RootElement;
            
            if (root.TryGetProperty("response", out var responseContent))
            {
                string rawJsonText = responseContent.GetString() ?? "";
                
                // Очистка от markdown
                rawJsonText = rawJsonText.Replace("```json", "").Replace("```", "").Replace("json", "").Trim();
                
                // На случай если модель начала отвечать с пояснений — обрезаем до первого {
                int startBrace = rawJsonText.IndexOf('{');
                if (startBrace > 0)
                    rawJsonText = rawJsonText.Substring(startBrace);
                
                int endBrace = rawJsonText.LastIndexOf('}');
                if (endBrace > 0 && endBrace < rawJsonText.Length - 1)
                    rawJsonText = rawJsonText.Substring(0, endBrace + 1);
                
                Console.WriteLine($"[Ollama] Очищенный JSON:\n{rawJsonText}");
                
                try
                {
                    using var parsedDoc = JsonDocument.Parse(rawJsonText);
                    var parsedRoot = parsedDoc.RootElement;
                    
                    if (parsedRoot.TryGetProperty("technical_spec", out var specElement))
                    {
                        var options = new JsonSerializerOptions { PropertyNameCaseInsensitive = true };
                        var spec = JsonSerializer.Deserialize<TechnicalSpecDto>(specElement.GetRawText(), options);
                        if (spec != null)
                            return spec;
                    }
                }
                catch (JsonException jex)
                {
                    Console.WriteLine($"[Ollama] Ошибка парсинга JSON: {jex.Message}");
                    Console.WriteLine($"[Ollama] Проблемный JSON: {rawJsonText}");
                }
            }
            
            return new TechnicalSpecDto { Subject = userPrompt };
        }
